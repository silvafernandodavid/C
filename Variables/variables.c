// Variables

#include <stdio.h>

int main(){
  int x;
  x = 5;

  printf("El numero de x = %i\n", x);

  float y;
  y = 10.5;

  printf("El numero de y = %f\n", y);

  double y2;
  y2 = 20.9;

  printf("El numero de y2 = %f\n", y2);

  char z;
  z = 'a';

  printf("El valor de z = %c\n", z);

  printf("y = %f, y2 = %f, x = % i, z = %c\n", y, y2, x, z);

  return 0;

}